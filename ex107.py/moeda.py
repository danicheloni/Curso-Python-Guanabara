def Aumentar(valor, perc):
    novo_valor = valor * (perc/100) + valor
    return novo_valor

def Dobro(valor):
    dobro = valor * 2
    return dobro

def Metade(valor):
    metade = valor / 2
    return metade

def Diminuir(valor, perc):
    novo_valor = valor - valor * (perc/100)
    return novo_valor

